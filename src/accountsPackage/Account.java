package accountsPackage;




public class Account {
	
	String firstName, lastName;
	int accountNumber;
	
	
	public void setFirstName(String firstnameset) {
		firstName = firstnameset;
	}
	
	public void setLastName(String lastnameset) {
		lastName = lastnameset;
	}
	
	public void setAccountNumber(int aNoset) {
		accountNumber = aNoset;
	}
	
	
	
	public String getFirstName() {
		return firstName;
	}
	
	public String getLastName() {
		return lastName;
	}
	
	public int getAccountNumber() {
		return accountNumber;
	}
	
	

	
}
